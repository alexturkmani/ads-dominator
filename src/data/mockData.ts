export const generateHourlyData = () => {
  return Array.from({ length: 24 }, (_, hour) => {
    const multiplier = hour >= 8 && hour <= 20 ? 1 + Math.random() * 0.5 : 0.3 + Math.random() * 0.3
    return {
      hour: `${hour.toString().padStart(2, '0')}:00`,
      impressions: Math.floor(500 * multiplier),
      clicks: Math.floor(15 * multiplier),
      conversions: Math.floor(1.5 * multiplier),
      spend: Math.round(20 * multiplier * 100) / 100,
    }
  })
}

