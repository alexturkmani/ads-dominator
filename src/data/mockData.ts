export const mockAnalyticsData: AnalyticsData[] = Array.from({ length: 30 }, (_, i) => {
  const date = new Date()
  date.setDate(date.getDate() - (29 - i))
  const baseImpressions = 8000 + Math.random() * 4000
  const clicks = Math.floor(baseImpressions * (0.02 + Math.random() * 0.02))
  const conversions = Math.floor(clicks * (0.03 + Math.random() * 0.02))
  const spend = clicks * (1.2 + Math.random() * 0.5)
  const revenue = conversions * (120 + Math.random() * 80)
  
  return {
    date: date.toISOString().split('T')[0],
    impressions: Math.floor(baseImpressions),
    clicks,
    conversions,
    spend: Math.round(spend * 100) / 100,
    revenue: Math.round(revenue * 100) / 100,
    ctr: Math.round((clicks / baseImpressions) * 10000) / 100,
    cpc: Math.round((spend / clicks) * 100) / 100,
    cpa: conversions > 0 ? Math.round((spend / conversions) * 100) / 100 : 0,
    roas: conversions > 0 ? Math.round((revenue / spend) * 100) / 100 : 0,
  }
})

